#include <numsim_codegen/targets/standalone_cxx.h>

#include <numsim_codegen/recipe.h>

#include <sstream>

namespace numsim::codegen {

auto StandaloneCxxTarget::emit(ConstitutiveModel const &model) const
    -> std::vector<EmittedFile> {
  // Emit the function first so the linalg-include decision tracks ACTUAL usage
  // in the emitted code, not a re-derived coupling predicate (PR #83 round-2
  // #4) — keying on the emitter's usage marker cannot drift from what was
  // emitted. The library is whatever default_linear_algebra_emitter() selects.
  std::string const body = model.emit_compute_function(m_la);
  bool const needs_la = body.find(m_la.usage_marker()) != std::string::npos;

  std::ostringstream os;
  os << "// Auto-generated by numsim-codegen. Do not edit.\n\n";
  os << "#pragma once\n";
  os << "#include <tmech/tmech.h>\n";
  os << "#include <cmath>\n";
  // Phase 3b-2b: a coupled local-Newton system's dense solve needs the linalg
  // backend's header(s) (e.g. <Eigen/Dense>).
  if (needs_la) {
    for (auto const &inc : m_la.includes()) {
      os << "#include " << inc << "\n";
    }
  }
  os << "\n";
  os << body;
  return {EmittedFile{model.name() + ".h", os.str(), "",
                      EmittedFile::Kind::Header}};
}

auto StandaloneCxxTarget::target_name() const -> std::string {
  return "StandaloneCxx";
}

} // namespace numsim::codegen
